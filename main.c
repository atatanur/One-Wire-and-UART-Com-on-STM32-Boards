/*This function turns the 16 bit number received from the thermometer to a double which makes sense*/
double byte_to_double (uint16_t sensor_value)
{
	double conv_result = 0;
	int shift = 0;

	for(int i2 = 0; i2 <= 10; i2++)
	{
		if((sensor_value >> shift) & 0x1)
		{
			conv_result +=  pow(2, (i2 - 4)) ;
		}
		shift++;
	}

	return conv_result;
}

